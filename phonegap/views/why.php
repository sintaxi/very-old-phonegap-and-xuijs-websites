    <h1>Why PhoneGap?</h1>
    
    <div class="blurb">
      <p>An express goal of the PhoneGap project is for the project to not
      exist. We believe in the web and devices should too. The web is moving
      off the desktop and into the pockets of people all over the world.
      Phones are the new window to the internet and, currently, they are
      second class. PhoneGap aims to move your device to a nice first class
      window. With a foot rest. Maybe a pillow.</p>
    </div>
    
    <h2 class="support">Simple</h2>
    <p>As simple as HTML, CSS and JavaScript. Software development is hard
    work. Device integration should be simple so you can focus on the
    application you are building instead of authoring complex
    compatibility layers. PhoneGap aims to solve device integration by web
    enabling devices native functionality with open standards.</p>
    
    <h2 class="support">Device Agnostic</h2>
    <p>The Apple iPhone, Google Android and various Blackberry models boast
    large screens and powerful device capability. Web enabling these
    devices in a consistant platform saves the developer from having to
    implement logic in three different software development paradigms.</p>
    
    <h2 class="support">Completely Open Source</h2>
    <p>We're interested in an open web and open standards.</p>
    
    <h2 class="support">Active Community</h2>
    <p>We need your help. A project this ambitious can only be successful
    with the entire web and mobile development community behind it.
    Contribute code on GitHub, discuss on Google Group and learn more on
    our developer Wiki.</p>
    
